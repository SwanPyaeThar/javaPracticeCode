package example;

public class OperatorInJava {
    //2ခုရှိမှအလုပ်လုပ်လို့ရရင် binary operetor လို့ခေါ်ပါတယ်။
    // '-' ကတော့uniary operator အ‌နေနဲ့အသုံးပြုလုိ့့ရပါတယ်။
    static void main(String[] args) {
        int n = 20;
        System.out.println(++n);
        System.out.println(n);
        System.out.println(n++);
        System.out.println(n--);
        System.out.println(n);
        System.out.println(--n);

    }
}
